a(2) = (-c4*Rvol - c5*(Rvol + r3) + c5*Rvol*x); %s^1
a(1) = -1; %s^0

ttt = ( b(3)*s^2 + b(2)*s + b(1) ) / ( a(3)*s^2 + a(2)*s + a(1) )

figure(1)
bode(ttt);

sToz2(b(3),b(2),b(1),a(3),a(2),a(1),96000);




